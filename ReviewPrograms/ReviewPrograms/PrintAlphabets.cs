using System;
using System.Collections.Generic;
using System.Text;

namespace ReviewPrograms
{
    class PrintAlphabets
    {
       public void Alphabets()
        {
            

                char ch;

                Console.WriteLine("Alphabets from a - z are: ");
                for (ch = 'a'; ch <= 'z'; ch++)
                {
                    
                    Console.Write(ch + "\n");
                }

                
            
        }

             
    }
}
